export function normalizeCategory(value: unknown): CanonicalClothingCategory | "" {
  if (typeof value !== "string") {
    return "";
  }

  const normalized = value.trim().toLowerCase().replace(/\s+/g, " ");
  return CATEGORY_ALIASES[normalized] ?? "";
}
